class Student
{
	//Add data members
	private String name;
	private String email;
	
	//Assign values
	 void setEmail(String email_add){
		 email=email_add;
	 }
	 void setName(String nm){
		 name=nm;
	 }
	 
	 //get values
	 String getEmail(){
		 return email;
	 }
	 String getName(){
		 return name;
	 }
	
}